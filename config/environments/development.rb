  config.hosts << "docs.app.localhost"
  config.hosts << "docs.com.localhost"
  config.hosts << "docs.org.localhost"
  config.hosts << "news.app.localhost"
  config.hosts << "news.com.localhost"
  config.hosts << "news.org.localhost"

