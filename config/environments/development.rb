  config.action_mailer.delivery_method = :smtp
  config.action_mailer.default_url_options = { host: "localhost", port: 3333 }
  config.action_mailer.smtp_settings = {
    address: ENV["RESEND_SMTP_ENDPOINT"],
    user_name: ENV["RESEND_SMTP_USER_NAME"],
    password: Rails.application.credentials.dig(:RESEND_SMTP_PASSWORD),
    port: 465,
    tls: true
  }

  # static file serve
  config.public_file_server.enabled = false

  # SMS Provider Configuration - Use test provider in development
  config.sms_provider = ENV.fetch("SMS_PROVIDER", "test")
